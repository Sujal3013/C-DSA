#include<iostream>
using namespace std;

int main()
{
    int a; //declaration
    a=12; //initialization

    cout<<"size of int"<<sizeof(a)<<endl;

    float b;
    cout<<"size of float"<<sizeof(b)<<endl;

    char c;
    cout<<"size of character"<<sizeof(c)<<endl;

    bool d;
    cout<<"size of boolean"<<sizeof(d)<<endl;

    short int li;
    cout<<"size of short integer"<<sizeof(li)<<endl;

    long int sli;
    cout<<"size of long int"<<sizeof(sli)<<endl;



    return 0;
}